def normalize_program(program: int) -> int:
    return max(0, min(127, int(program)))


def normalize_channel(channel: int) -> int:
    return max(0, min(15, int(channel)))


def is_drum_channel(channel: int) -> bool:
    return normalize_channel(channel) == DEFAULT_DRUM_CHANNEL


def effective_midi_bank(channel: int, bank: int = 0) -> int:
    if is_drum_channel(channel):
        return DEFAULT_DRUM_BANK
    return max(0, int(bank))


def gm_program_name(program: int, *, channel: int | None = None) -> str:
    normalized_program = normalize_program(program)
    if channel is not None and is_drum_channel(channel):
        return GENERAL_MIDI_DRUM_KITS.get(normalized_program, f"Drum Kit {normalized_program + 1:03d}")
    return GENERAL_MIDI_PROGRAMS[normalized_program]


def gm_program_label(program: int, *, channel: int | None = None) -> str:
    normalized_program = normalize_program(program)
    return f"{normalized_program + 1:03d} - {gm_program_name(normalized_program, channel=channel)}"
